package first.join.model;

public class Member {
	private String id;
	private Integer pw;
	private String name;
	private int age;
	private String email;
	
	public String getId() {
		return id;
	}
	public Integer getPw() {
		return pw;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public String getEmail() {
		return email;
	}
	
	
}
